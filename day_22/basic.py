import streamlit as st 

# let's built the simple webapp using the python script

# this print the hello world in browser
st.title("Hello world")

# setting header
st.header("Welcome to the world of AI")

# setting subheader
st.subheader("This is subheader")

# to give any information
st.info("Hi i am python")

# showing the warning message
st.warning("Something went wrong")

# write function 
st.write("Hello I am wrting something")

# showing the error message
st.error("Something went wrong")

# we can even print the success message 
st.success("congratulation")